package com.javaguru.lessons.lesson2;

public class MathOperationsDemo {
     public static void main(String[] args) {
        System.out.println("That simple math dude!");
        int firstNumber = 21;
        int secondNumber = 4;

        System.out.println("Slozenie");
        System.out.println(firstNumber + secondNumber);

        System.out.println("Vichitanie");
        System.out.println(firstNumber - secondNumber);

        System.out.println("Umnozenie");
        System.out.println(firstNumber * secondNumber);

        System.out.println("Delenie");
        System.out.println(firstNumber / (double)secondNumber);

        System.out.println("Ostatok");
        System.out.println(firstNumber % secondNumber);

    }
}
